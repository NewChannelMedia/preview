      $(document).ready(function(){
        var speed = 400;
        var first = 0;
        var pause = 5000;

        function tick() {
            first = $('#slider1 ul li:first').html();
            $('#slider1 ul li:first').animate({
              opacity: 0,
              height: "toggle"
            }, speed, function() {
                $(this).remove();
                last = '<li>' + first + '</li>';
                $('#slider1 ul').append(last);
            });
        }

        $('#slider1 ul').click(function() {
            tick();
            return false;
        });

        setInterval(tick, pause);
      });
